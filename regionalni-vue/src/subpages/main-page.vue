<script>
import MainHeader from "@components/main-header.vue";
import MainFooter from "@components/main-footer.vue";
import MainCookies from "@components/main-cookies.vue";
import VueScrollTo from "vue-scrollto";
import axios from "axios";

export default {
  components: {
    MainHeader,
    MainFooter,
    MainCookies,
  },
  data() {
    return {
      formData: {
        name: "",
        surname: "",
        email: "",
        phone: "",
        message: "",
        acceptTerms: false,
      },
      validationMessage: "",
    };
  },
  methods: {
    sendForm() {
      this.validationMessage = "";

      if (
        !this.formData.name ||
        !this.formData.surname ||
        !this.formData.email ||
        !this.formData.phone ||
        !this.formData.message
      ) {
        this.validationMessage = "Wszystkie pola muszą zostać wypełnione.";
      }

      if (this.formData.acceptTerms) {
        axios
          .post("http://localhost:8000/send-form", this.formData)
          .then((response) => {
            alert(
              "Wiadomość została wysłana. Wkrótce się z tobą skontaktujemy!"
            );
            this.clearFields();
          })
          .catch((error) => {
            console.error(error);
          });
      } else {
        this.validationMessage =
          "Musisz zaakceptować politykę prywatności i regulamin sklepu.";
      }
    },
    clearFields() {
      this.formData.name = "";
      this.formData.surname = "";
      this.formData.email = "";
      this.formData.phone = "";
      this.formData.message = "";
      this.formData.acceptTerms = "";
      this.validationMessage = "";
    },
    checkLocation() {
      const currentPath = this.$route.fullPath;

      if (currentPath === "/#home") {
        VueScrollTo.scrollTo("#home", 500, { easing: "ease-in-out" });
      } else if (currentPath === "/#project") {
        VueScrollTo.scrollTo("#project", 500, { easing: "ease-in-out" });
      } else if (currentPath === "/#partner") {
        VueScrollTo.scrollTo("#partner", 500, { easing: "ease-in-out" });
      } else if (currentPath === "/#company") {
        VueScrollTo.scrollTo("#company", 500, { easing: "ease-in-out" });
      } else if (currentPath === "/#contact") {
        VueScrollTo.scrollTo("#contact", 500, { easing: "ease-in-out" });
      }
    },
  },
  mounted() {
    this.checkLocation();
  },
};
</script>
